Array2D* currMaze;


bool checkwall(int x, int y)
{
	if ((*currMaze)[x][y] == '#')
		return true;
	else
		return false;
}

int checkcell(int x, int y)
{
	if ((*currMaze)[x][y] == ' ')
		return 0;
	else if ((*currMaze)[x][y] == '.')
		return 1;
	else if ((*currMaze)[x][y] == '0')
		return 2;
	else if ((*currMaze)[x][y] == 'C')//c is pacman
		return 3;
	else
		return 4;	//G is ghost
}